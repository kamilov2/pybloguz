# Импортируем библиотеку Pandas
import pandas as pd

# Создаем простой набор данных - таблицу с данными о некоторых студентах
data = {
    'Имя': ['Алексей', 'Елена', 'Иван', 'Мария', 'Петр'],
    'Возраст': [25, 24, 22, 28, 30],
    'Средний балл': [4.5, 4.8, 3.9, 4.2, 4.0]
}

# Создаем DataFrame, основанный на наших данных
df = pd.DataFrame(data)

# Выводим первые несколько строк DataFrame
print("Первые строки данных:")
print(df.head())

# Выводим информацию о данных
print("\nИнформация о данных:")
print(df.info())

# Вычисляем среднее значение среднего балла студентов
mean_grade = df['Средний балл'].mean()
print("\nСредний балл студентов:", mean_grade)
